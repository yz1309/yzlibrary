    /**
     * 获取字符串中的首字拼音
     *
     * @param input input
     * @return String
     */
    public static String converFirstName(String input) {
        if (null == input) {
            return "";
        }
        int len = input.length();
        for (int i = 1; i < len; i++) {
            String firstName = map.get(input.substring(0, input.length() - i));
            if (null != firstName) {
                return firstName.toUpperCase();
            }
        }
        return PingYinUtil.getPingYin(input);
    }

    /**
     * 获取字符串中的全部拼音
     *
     * @param input input
     * @return String
     */
    public static String converFullName(String input) {
        if (null == input) {
            return "";
        }
        int len = input.length();
        for (int i = 1; i < len; i++) {
            String firstName = map.get(input.substring(0, input.length() - i));
            if (null != firstName) {
                String lastName = PingYinUtil.getPingYin(input.substring(input.length() - i));
                return firstName.toUpperCase() + lastName;
            }
        }
        return PingYinUtil.getPingYin(input);
    }
